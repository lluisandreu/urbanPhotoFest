				<h2>The Abstract Beach</h2>
	            
				<p><b>The Tutor:</b> Ida Pap is a self-taught photographer living in East London. Growing up in central Budapest she has an odd sympathy for urban decay and loves the abandoned feeling of the city. She believes in seeing beyond the average to capture those details people are too busy to pay attention to, using often unseen treasures as main subjects. The people in her photography are secondary and mostly coincidental, thus she doesn’t concentrate on documenting people in their environments but the environment itself. Her body of urban work includes urban abstract, architectural, conceptual and street portraits. Please visit <a href="http://www.ideoda.com" target="_blank">www.ideoda.com</a> for examples of her work. </p>
				 
