				<h2>Typology and the Beautiful Nothing</h2>
	            
				<p><b>The Tutor:</b> While studying architecture in Canada, Karl Grupe started using photography to document projects, fell in love with the medium and never looked back. Since then he has worn many hats within the industry, which include editor, talent scout, researcher, lecturer, curator, writer and photographer. He is an Associate Lecturer at Central Saint Martins College of Art and Design and an MRes Design student at Goldsmiths College where he is researching smartphone photography by non-professionals as a developing vernacular within brand campaigns.
				
				<p><a href="http://www.karlgrupe.com/blog">karlgrupe.com/blog</a><br>Instagram: stuffcarlsees</p>
				 
				<p><b>The Workshop:</b> Led by Karl Grupe, this workshop will explore the method of communicating through the use of typology. Participants will be introduced to the abstract of typology, from its origins to contemporary use in photography and be given examples of how it is used effectively by image makers as a visual communication device. Participants will then have the opportunity to approach the urban environment through the typological lens in a practical photographic exercise designed to shift the way photographers "see" and perform under this context. </p>
				
				<p><b>Needed:</b> Camera, memory cards/film, spare batteries, warm, comfortable clothing, sensible footwear.<br/><br/>Please note: Film and processing costs are not included.</p>
				
