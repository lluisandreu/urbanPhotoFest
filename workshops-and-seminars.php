				<h2>Retrospeculations: <br>Reconstructing a Past to Come </h2>
	            
				<p><b>The Discussants</b> Nick Scammell is fascinated by where the photograph meets the poem and where the image meets the object. His debut collection of poetry, London: Ghost Autopsy, was a Stride Magazine book of the year for 2009 and accompanied his first solo photographic exhibition of the same name. Nick curates and exhibits regularly with Crossing Lines, contributes to fLIP Magazine and was a part of the London Villages Project. He is presently working on an experimental book project - entitled Significant Wreckage - as well as a long-term project on London's independent second-hand bookshops, Archipelagoes. Studying for a photographic MA at LCC, Nick's practice orbits memory and failure via the everyday city.</p>
				 
				<p>John Levett is a Visiting Research Fellow in the Centre for Urban & Community Research, Goldsmiths College. He is the convenor of both the Crossing Lines Group and the Greenwich Group of London Independent Photography and organizer of The London Villages Project.</p>
				
				<p><b>The Seminar:</b> <em>How can a relationship with the past exist in which memory functions as an archive process, allowing continual reconsideration, rather than as a form of entombment?</em> Renée Green</p>
				
				<p>A gathering of gatherings made over twenty years; an unfolding; a condensation (Freud) of tremendous fragments (Klee) - in this work, John Levett will respond to Green's question by way of Blanchot's proposal of a past to come, and in that spirit re-engineer, re-purpose and re-imagine his archive.</p>
				
				<p>Mutations of connection and disconnection (Foster) are hoped for: new lines of interpretation, counter-archives within the archive. Gaps and absences will be taken as forms of evidence (Merewether) for this new prospective past (Demos) and the freedoms it may offer.</p>
				
				<p>A presentation will be given by both artist and curator; exploring the tensions between the following statements:</p>
	
				<p><em>The archive always works against itself.</em> Derrida</p>
	
				<p><em>Memory restores possibility to the past.</em> Dimsdale</p>
				
