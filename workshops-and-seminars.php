				<h2>A Two Way Street:<br>Theory into Practice, Practice into Theory</h2>
	            
				<p><b>The Discussants:</b> Hugh Look is Chair of London Independent Photography and founded LIP's Central London Group. His interest in the relationship between theory and practice in photography was stimulated by attending the Crossing Lines group and in conversation with other photographers, many of them members of LIP.</p>
				 
				<p>John Levett is a Visiting Research Fellow in the Centre for Urban & Community Research, Goldsmiths College. He is the convenor of both the Crossing Lines Group and the Greenwich Group of London Independent Photography and organizer of The London Villages Project.</p>
				
				<p><b>The Seminar:</b> This event is designed to stimulate and enlighten practising photographers about the value of theory to their practice, and to show that theory also evolves in response to the changing practice and (importantly) role of photography in societies, politics, the gallery; on screen and on the page. ‘Theory’ can seem distant and detached from the making of actual photographs but many have found that theory can both stimulate their work and invigorate their imagination.</p>
				
				<p>We aim to explore the value of theory to photographers working within the urban sphere: theories of perception and ‘seeing’; the role of photography, anthropology and ethnography; socio-political thinking; communications and aesthetics.</p>
				
				<p>The session will consist of four presentations by practicing photographers whose work is both informed and sustained by an appreciation of ‘theory’ as it relates to their practice within the urban sphere. Open discussion will follow. Hugh Look & John Levett will chair the session & the panel will consist of: Professor Christopher Wainwright, Claire Levy, Judith Jones, Michael Rodgers.</p>
				
				
