            <h2>Masterclass with Rut Blees Luxemburg</h2>
            
            <h3>About the Artist</h3>
            <p>Urban Photo Fest is pleased to announce this year’s masterclass will be presented by the international photographic artist, Rut Blees Luxemburg. Rut’s work focuses on the relationship between urban landscapes, often photographed at night, and the surfaces of everyday city spaces. Her images have a shimmering quality, rendering urban spaces, hitherto seen as possibly banal and uninteresting, as radiating with energy, light and a sense of the uncanny. Rut’s work is particularly relevant to this year’s festival theme of ‘urban materialities’. 	</p>
 
            <h3>About the masterclass</h3>
            <p>Rut has extensive experience of image-making within the urban domain and after an initial presentation of her work, followed by a discussion about related theoretical and technical issues, participants will introduce their images focusing on wider debates around the concepts of urban materiality and landscape. Participants will be given a working brief based on this theme, and will then work on producing a number of images to bring back to the group for feedback and critique, and for individual feedback from Rut at the conclusion of the masterclass. There will be a public projection of the work made on the concluding day of the festival. All participants will receive a certificate of attendance upon successful completion, and there is a maximum of 14 places.</p>
            
            <h3>What you will need</h3>
			<p>You can work with either analogue or digital cameras for you assignment, and if you are planning to work with the former, you will need to identify a lab that can process your films and reference prints within a 1-3 hour period in order to have the images ready for the following day. If you are working digitally, the images will need to be edited and put into a PowerPoint format for next day feedback. The sessions involve walking around urban landscapes, so you are advised to bring appropriate clothing, water, light-meter, tripod, and shutter cable-release.</p>
			
			
