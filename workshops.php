			<h1>Workshops &amp; Seminars</h1>
			<h3>Migrations &amp; Mobilities: living through photography</h3>
			<p>This session reflects upon the experiences of four photographers — journeys within photography, between continents, through landscapes, inside families; movement within private spaces, political spaces, material &amp; imagined spaces.</p>
			<p>The photographers will consider matters of pausing, stillness, personal remembrances, objects &amp; emotional places — the place of memory &amp; memorialised worlds in photographic practice.</p>
			<p>The work of each photographer will be illustrated in the context of each of their personal histories &amp; autobiographies.</p>
			<p>The session will acknowledge the position of the distinctive personal archive as central to each photographer’s mode of engagement with their unique journeys &amp; their mode of presenting their practice.</p>
			<p>It is intended that materials from the archive of each photographer will be available for viewing &amp; discussion with the presenters on conclusion of the formal session.</p>
