            <h3>About the photographer</h3>
			<p>Urban Photo Fest is pleased to announce this year’s masterclass will be presented by 
				international photographer Peter Marlow, former president of Magnum Photos. Peter’s work 
				focuses on apparently incidental scenes that speak of social relations. His images have a 
				compelling poetic quality and address notions of stasis, movement and change.</p>

            <h3>About the masterclass</h3>
			<p>Following an initial presentation of his work and discussion of related theoretical and 
				technical issues, participants will introduce their images. The Masterclass will focus on how to 
				build a visual story and participants will be given a working brief based on the themes 
				discussed, and then work on producing a number of images to bring back to the group for 
				feedback and critique. </p>

            <h3>What you will need</h3>
			<p>In order to participate you will need to submit a short description of the visual story that you 
				intend to develop during the Masterclass along with a portfolio (up to 10 images)</p>
			<p>Due to the duration of the course participants are advised to produce and edit their work 
				digitally. The images will need to be edited and put into a Power Point format for next day 
				feedback. </p>			
			
