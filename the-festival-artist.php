			<p><b>Tatiana Macedo</b> works mainly with photography and film and their expanded forms.</p> 


			<p><b>About</b> <em>Seems So Long Ago, Nancy</em></p>

			<p>The gallery or visitor assistant in a museum or art gallery is a fundamental piece of the institution’s mise-en-scéne. Seems So Long Ago Nancy is the title of an experimental documentary that resulted from a visual, sensorial and ethnographic research at Tate Britain and Tate Modern. It focuses on the people who perform Visitor Assistance roles in these galleries and their subjective experience of place and time. The camera fluctuates between the subjects and the architectural post-modern and neoclassical spaces they occupy, small gestures choreographed in an elliptical narrative, in and outside the walls of the personal, architectural and social fabrics.</p>


			<h3>Biographical Note</h3>

			<p><b>Tatiana Macedo</b> (b. 1981, Lisbon, Portugal) Lives and works in Lisbon and London. Studied at Central St. Martins College of Art (BA Fine Arts), the Faculty of Human and Social Sciences, New University of Lisbon (MA Visual Anthropology) the London College of Communication (BTEC Studio and Location Photography) and the Calouste Gulbenkian Foundation’s Creation and Creativity Programme Advanced Photography Course, Lisbon (2005). She was artist in residence at the CAM - Modern Art Centre, Lisbon (2007). In 2012 Macedo premiered her first feature film - Seems So Long Ago, Nancy, an experimental documentary shot at Tate Modern and Tate Britain, London (2011). The film was screened in several Film Festivals and Art galleries in the UK, The Netherlands, Portugal, Spain, Macau, Argentina and Peru. A previous version of this film was screened in Amsterdam as part of the Stedelijk Museum Bureau Video Program Really Exotic? and a work in progress version was screened at Tate Britain (2012) together with a discussion with curator Paul Goodwin as part of the Late At Tate Program (2012). Recent solo exhibitions include Seems So Long Ago, Nancy at 4 Windmill St. Gallery, London and recent group exhibitions include Jaz Aqui, Na Pequena Praia Extrema, ZDB Art Centre, Lisbon (2013) and Visões do Desterro, Caixa Cultural do Rio de Janeiro, Brazil (2013).</p>
			
