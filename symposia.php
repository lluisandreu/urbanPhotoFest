	            <h2>Urban Encounters:<br>Urban Materialities Symposium</h2>
	            
	            <p>Focusing on the theme of Urban Materialities, this year’s Urban Encounters symposium will highlight practices from a range of disciplines - including architecture, archaelogy, visual arts and geography - that engage with notions of thing-ness. The interdisciplinary contributions bring distinctive, yet overlapping concerns to the investigation, critical analysis and theorizing of materialities within the urban context. These can include geographies of security and surveillance, bodies, surfaces, everyday objects, or larger architectural forms.</p>
	 
	            <p>Panels engage with these concepts and practices through the themes of Sensibilities, Objecthood, and Disappearances. </p>
	            
	            <p>Speakers are <b>Peter Fraser</b> ‘A City in the Mind’, <b>Adrian Lahoud</b>, <b>Corinne Silva</b> ‘Gardening the Suburbs: the Politics of Planting in Israel/Palestine’, <b>Paola Yacoub</b> ‘Under this Trail there are Corpses’, <b>Tatiana Macedo</b>, <b>Paul Goodwin</b>, <b>Cristoph Lueder</b> ‘Upside Down and Sideways Up – Corporeality in Translations between Ground and Image Plane’, <b>Nick Ferguson</b> ‘Self-centred Objects: Suburbia, Intervention and Photographic Representation’, <b>Dan Hays</b> ‘Touch Screen (screen as landscape)’, <b>Isaac Marrero-Guillamón</b> ‘Contesting the Tyranny of Redevelopment: Critical Art and the London Olympics’, <b>Paul Halliday</b>, <b>Rachel Jones</b> ‘Landscape of Disappearances’ and <b>Caroline Knowles</b>.</p>	            
	            
	            <p>This symposium is organised in collaboration with the Centre for Urban and Community Research at Goldsmiths, University of London and Kingston University’s Faculty of Art, Design and Architecture.</p>
	            
