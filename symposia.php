				<h2>Visual Urbanism:<br>Perceptions of the Material Landscape</h2>
				
				<p>How do material aspects of the urban landscape inform and inspire urban research? How can visual research methods enable urban researchers to investigate the city’s immaterialities? How do images shape the way we view the material form of the city? Following the success of last year’s event, researchers and practitioners from across the humanities and social sciences will come together to discuss contemporary concerns within visual urbanism in the second annual conference of the International Association of Visual Urbanists. An emphasis will be placed on innovative methods within current urban research relating to the arts, humanities and social sciences.</p>
	 
				<p>The conference will comprise presentations and panel discussions examining interdisciplinary approaches to investigating the materiality of the urban landscape. A keynote talk will be given by Professor Caroline Knowles (Goldsmiths, University of London). The event will include a short film festival showcasing work by artists and researchers exploring the urban landscape through moving images.</p>
				
				<p>Refreshments, a sandwich lunch and wine reception are included in the ticket price.</p>
				
