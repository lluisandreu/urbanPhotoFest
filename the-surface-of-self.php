            <h2>Photofusion Exhibition: The Surface of Self</h2>

            <br>In Partnership with Urban Photo Fest

			<p>The Surface of the Self is an exhibition that brings together artists Laura Cuch (Spain), Anastassios Kavassis (Greece) and Orly Zailer (Israel), whose work in contemporary photographic portraiture raises questions around human subjectivity and cultural identity. Laura and Anastassios work with analogue cameras – a Rolleiflex 6x6 Twin Lens and an antique wooden camera – two technological apparatus that mediate their ways of engaging with people and make the process of taking portraits a ritual.  On the other hand, Orly has worked with a digital camera for both the video and stills parts of the project. She aims to distinguish between the periods in which different media were used to produce the old and new. </p>

			<p>These portraiture projects are concerned with ritual. Such rituals have enabled Laura (<em>Sleepless</em>), Anastassios (<em>Athenians</em>) and Orly (<em>The Time Elapsed Between Two Frames</em>) to produce collections of images that invite the audience to reflect on broader social and cultural issues, by embodying difference and questioning cultural norms. The images address how people that barely sleep make sense of such a condition? What it means to be an Athenian today? How one’s identity is affected by the physical resemblance to one’s family antecedents? In short, they are concerned with the mutable self inscribed, in material terms, on the surface of bodily being.</p>

